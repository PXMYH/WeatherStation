//  reading the CSV data entries into 2D Vectors
bool weather::loadFile () {
    // locate weather source data CSV file
    string weather_data_src = "BCnorthWeatherData.csv";

    ifstream in;
    in.open("BCnorthWeatherData.csv");
    if (in.is_open()) {std::cout << "File is opened!" << endl;}
    else std::cout << "File is not FOUND!" << endl;

    // open weather source data
    ifstream file_handle(weather_data_src.c_str());
    if (!file_handle)
        //error ("Can't open weather data source file ", weather_data_src);
			return false;

    // load data into pre-defined structure
    std::cout << "LOADING DATA ..." << endl;

	string line, field;
	vector<string> data;

	// get next line in file
    while ( getline(file_handle,line) )    {
        data.clear();
        stringstream ss(line);

		// break line into comma delimitted fields
        while (getline(ss,field,','))  {
            data.push_back(field);  // add each field to the 1D array
        }

        record.push_back(data);  // add the 1D array to the 2D array
    }
    std::cout << "DATA LOADED." << endl;

    // close weather source data
    // this could be implicitly closed when we are leaving the scope of "load_file" function
    file_handle.close();
	return true;
}

